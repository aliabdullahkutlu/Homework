# 1. HTML
# HTML: Hyper Text Markup Language olarak adlandırılan bir işaret dili veya kodlama dilidir. HTML, web sayfalarının yapısını, içeriğini ve görünümünü belirlemek için kullanılır.

# 2.HTML Locators
# ID Locator                  ==> Web sayfasındaki öğelerin ID özelliklerini kullanarak belirler.

# Name Locator                ==> Web sayfasındaki öğelerin Name özelliklerini kullanarak belirler.

# Class Locator               ==> Web sayfasındaki öğelerin Class özelliklerini kullanarak belirler.

# Tag Name Locator            ==> Web sayfasındaki öğelerin etiket isimlerini kullanarak belirler.

# Link Text Locator           ==> Web sayfasındaki bağlantıların metin içeriğini kullanarak belirler.

# Partial Link Text Locator   ==> Web sayfasındaki bağlantıların kısmi metin içeriğini kullanarak belirler.

# CSS Locator                 ==> Web sayfasındaki öğelerin CSS seçicilerini kullanarak belirler.

# XPath Locator               ==> Web sayfasındaki öğelerin XPath ifadelerini kullanarak belirler.

# 3. SELENİUM'DA AKSİYONLAR
# click()                ==> Seçilen öğeye tıklamak için kullanılır.

# clear()                ==> Seçilen öğenin içeriğini temizlemek için kullanılır.

# Switch to Window       ==> Bir pencereye geçmek için kullanılır.

# submit                 ==>  Bir formu göndermek için kullanılır.

# select_by_visible_text ==>  Bir dropdown öğesinde belirli bir metne tıklamak için kullanılır.

# Refresh()              ==>  Sayfayı yenilemek için kullanılır.

# Switch to Frame        ==>  Bir iframe içine geçmek için kullanılır.

# Scroll                 ==>  Sayfayı aşağı veya yukarı kaydırmak için kullanılır.

# Back()                 ==>  Önceki sayfaya gitmek için kullanılır.

# Forward()              ==>  Sonraki sayfaya gitmek için kullanılır.

# find_element_by_id():           Bir öğeyi ID'siyle bulmak için kullanılır.

# find_element_by_name():         Bir öğeyi adıyla bulmak için kullanılır.

# find_element_by_xpath():        Bir öğeyi XPath kullanarak bulmak için kullanılır.

# find_element_by_css_selector(): Bir öğeyi CSS seçicisi kullanarak bulmak için kullanılır.

# ==> Bu metodlar, belirli bir öğeyi veya öğeleri bulmak ve ardından aksiyonlar gerçekleştirmek için kullanılır. Bu şekilde, web uygulamalarının otomasyonu daha kolay ve daha etkili hale gelir.

